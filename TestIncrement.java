public class TestIncrement
{
public static void main(String[] args)
{
int num1;
int num2;
num1=10;
System.out.println("first number is:"+num1);
num2=20;
System.out.println("second number is:"+num2);

num1++;
System.out.println("first number after increment is: "+num1); 
num2--;
System.out.println("second number after decrement is: "+num2);
}
}