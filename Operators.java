public class Operators
{
public static void main(String[] arg)
	{
	int num1;
	int num2;
	int sum;
	int diff;
	int mult;
	int div;
	int rem;
	num1=20;
	num2=80;
	rem=num1%num2;
	diff=num1-num2;
	mult=num1*num2;
	div=num2/num1;
	sum=num1+num2;
	System.out.println("sum= "+sum);
	System.out.println("diff= "+diff);
	System.out.println("mult= "+mult);
	System.out.println("div= "+div);
	System.out.println("remainder: "+rem);
	}
}
