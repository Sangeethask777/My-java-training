public class SwappingTest
{
public static void main(String[] args)
{
int a,b;
int temp;
a=10;
b=20;
System.out.println("before swapping 1st number : "+a);
System.out.println("before swapping 2nd number : "+b);
temp=a;
a=b;
b=temp;

System.out.println("after swapping 1st number : "+a);
System.out.println("after swapping 2nd number : "+b);

}
}

